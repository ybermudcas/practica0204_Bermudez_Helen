diccionario = {}

nombre = input("¿Cuál es tú nombre?")
diccionario["nombre"] = nombre
print(diccionario)

edad = int(input("¿Cuál es tú edad?")) 
diccionario["edad"]  = edad
print(diccionario)

sexo = input("¿Cuál es tú sexo?")
diccionario["sexo"] = sexo
print(diccionario)

móvil = input("¿Cuál es tú número de móvil?")
diccionario["móvil"] = móvil
print(diccionario)

email = input("¿Cuál es tú correo electrónico?")
diccionario["email"] = email
print(diccionario)